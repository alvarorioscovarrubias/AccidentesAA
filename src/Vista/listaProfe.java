    private void cargarTabla(){
        cargarColumna();
        int est = 1;
        for (Usuario u : controller.findUsuarioEntities()) {
            String apellido = u.getPaterno() +" "+ u.getMaterno();
            String estado = "";
            Date fecha = u.getFechaNacimiento();
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            String fechaTexto = form.format(fecha);
            if (u.getEstado().equals(BigInteger.valueOf(est))) {
                estado = "Activo";
            }else{
                estado = "Dado de Baja";
            }
            modelo.addRow(new Object[]{u.getIdUsuario(), u.getRut(),u.getNombre(), apellido, fechaTexto, u.getEmail(), u.getCelular(), u.getTelefono(), u.getDireccion(), estado});
        }
    }
        private void cargarTablarPorCodigo(String rut, TipoUsuario tipoUsuarioIdTipoUsuario) {
        cargarColumna();
        int est = 1;
        if (!rut.isEmpty() && !rut.trim().equals("")) {   
        for (Usuario u : controller.buscarUsuario(rut, tipoUsuarioIdTipoUsuario)) {
            String apellido = u.getPaterno() +" "+ u.getMaterno();
            String estado = "";
            Date fecha = u.getFechaNacimiento();
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            String fechaTexto = form.format(fecha);
            if (u.getEstado().equals(BigInteger.valueOf(est))) {
                estado = "Activo";
            }else{
                estado = "Dado de Baja";
            }
            modelo.addRow(new Object[]{u.getIdUsuario(),u.getRut(),u.getNombre(), apellido, fechaTexto, u.getEmail(), u.getCelular(), u.getTelefono(), u.getDireccion(), estado});
        }
        }else{
            JOptionPane.showMessageDialog(this, "No se encontro el rut ingresado en la base de datos.");
            cargarTabla();
            limpiar();
        }

    }
    private void cargarColumna(){
        modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Rut");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Fecha Nacimiento");
        modelo.addColumn("Email");
        modelo.addColumn("Celular");
        modelo.addColumn("Telefono");
        modelo.addColumn("Direccion");
        modelo.addColumn("Estado");
        lstUsuario.setModel(modelo);
    }
    private void limpiar(){
        txtBuscadorProfesional.setText("");
    }

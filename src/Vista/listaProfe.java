    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        int fila = lstUsuario.getSelectedRow();
        if (fila != -1) {
            Object codigo = lstUsuario.getValueAt(fila, 0);
            String cod = String.valueOf(codigo);
            int codigoC = Integer.parseInt(cod);
            try {
                int estado = 2;
                usuario = controller.findUsuario(BigDecimal.valueOf(codigoC));
                usuario.getIdUsuario();
                usuario.getRut();
                usuario.getPaterno();
                usuario.getMaterno();
                usuario.getNombre();
                usuario.getFechaNacimiento();
                usuario.getEmail();
                usuario.getClave();
                usuario.getCelular();
                usuario.getTelefono();
                usuario.getDireccion();
                usuario.setEstado(BigInteger.valueOf(estado));
                usuario.getFechaCreacion();
                usuario.setFechaModificacion(fecha);
                usuario.getTipoUsuarioIdTipoUsuario();
                controller.edit(usuario);
                JOptionPane.showMessageDialog(this, "Se ha Desactivado el Profesional", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                cargarTabla();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Validación", 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor seleccionar la fila que desea desactivar", "Validación", 0);
        }
    }//GEN-LAST:event_btnBajaActionPerformed


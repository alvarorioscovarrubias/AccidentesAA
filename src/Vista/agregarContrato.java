    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int fila = tblEmpresa.getSelectedRow();
        if (fila != -1) {
            Object codigo = tblEmpresa.getValueAt(fila, 0);
            String cod = String.valueOf(codigo);
            int idTipoPago = cbPago.getSelectedIndex();
            String desPago = cbPago.getSelectedItem().toString();
            tipoPago = new TipoPago(BigDecimal.valueOf(idTipoPago), desPago);
            int codigoEmpresa = Integer.parseInt(cod);
            int contador = 0;
            boolean valor = false;
            do{
                contador++;
                valor = controllerCon.buscarCodigo(BigDecimal.valueOf(contador));
            }while(valor == true);
            try{
                empresa = controllerEmp.findEmpresa(BigDecimal.valueOf(codigoEmpresa));
                empresa.getIdEmpresa();
                empresa.getNombre();
                empresa.getRazonSocial();
                empresa.getDireccion();
                empresa.getTelefono();
                empresa.getFechaCreado();
                empresa.getFechaModificado();                
                //aumentar 12 meses el fin del contrato
                if(validarFechas()){
                Date d = dateInicio.getDate();
                Calendar c1 = Calendar.getInstance();
                c1.setTime(d);
                c1.add(Calendar.MONTH,12);
                Date fin = c1.getTime();                
                contrato = new Contrato();
                contrato.setIdContrato(BigDecimal.valueOf(contador));
                contrato.setFechaInicio(dateInicio.getDate());
                contrato.setFechaTermino(fin);
                contrato.setEmpresaIdEmpresa(empresa);
                contrato.setEstadoContratoIdEstCon(estadoContrato);
                controllerCon.create(contrato);
                //creacion de cantidad de cuotas
                String cuotas = txtCantCuotas.getText();
                int cantCuotas = Integer.parseInt(cuotas);
                String monto = txtMontoTotal.getText();
                int montoTotal = Integer.parseInt(monto);
                int montoCuotas = montoTotal/cantCuotas;
                Date fechaPago = dateFechaPago.getDate();
                Calendar c = Calendar.getInstance();
                Calendar vencimiento = Calendar.getInstance();
                c.setTime(fechaPago);
                vencimiento.setTime(fechaPago);
                for (int i = 1; i <= cantCuotas; i++) {
                    boolean v = false;
                    int cont=0;
                    do {                        
                        cont++;
                        v = controllerCuota.buscarCodigo(BigDecimal.valueOf(cont));
                    } while (v == true);
                    
                    vencimiento.add(Calendar.DATE, 10);
                    cuota = new Cuota();
                    cuota.setIdCuota(BigDecimal.valueOf(cont));
                    cuota.setMonto(BigInteger.valueOf(montoCuotas));                   
                    cuota.setFechaPago(c.getTime());
                    cuota.setFechaVencimiento(vencimiento.getTime());                                           
                    cuota.setMonto(BigInteger.valueOf(montoCuotas));
                    cuota.setContratoIdContrato(contrato);
                    controllerCuota.create(cuota);
                    pago = new Pago();
                    pago.setIdPago(BigDecimal.valueOf(cont));
                    pago.setFechaPago(c.getTime());
                    pago.setCuotaIdCuota(cuota);
                    pago.setEstadoPagoIdEstadoPago(estadoPago);
                    pago.setTipoPagoIdTipoPago(tipoPago);
                    controllerPago.create(pago);
                    c.add(Calendar.MONTH, 1);
                    }
                }
                JOptionPane.showMessageDialog(this, "Contrato empresa "+ empresa.getNombre()+" agregado con exito", "Validación", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage(), "Validación", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No dejar valores en blanco", "Validación", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed


    private void cargarTabla(){
        cargarColumna();
        for (Empresa e : controllerEmp.findEmpresaEntities()) {
            Date fechaCreado = e.getFechaCreado();
            Date fechaModificado = e.getFechaModificado();
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            String fechaText = form.format(fechaCreado);
            String fecha = form.format(fechaModificado);
            modelo.addRow(new Object[]{e.getIdEmpresa(), e.getNombre(), e.getRazonSocial(), e.getDireccion(),e.getTelefono(),fechaText,fecha});
        }
    }
    private void cargarColumna(){
        modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Razon Social");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Fecha Creado");
        modelo.addColumn("Fecha Modificado");
        tblEmpresa.setModel(modelo);
    }
    private void limpiar(){
        dateInicio.setDate(null);
        dateFechaPago.setDate(null);
        txtCantCuotas.setText("");
        txtMontoTotal.setText("");
        cbPago.setSelectedItem("Seleccione");
    }
    
    private boolean validarFechas(){
        boolean valor = false; 
        Date fecha1 = dateInicio.getDate();
         Date fecha2 = dateFechaPago.getDate();
        
        if(fecha1.before(fecha2) || fecha1.equals(fecha2)){
          valor = true;
          JOptionPane.showMessageDialog(this, "La fecha es válida", "Validación", JOptionPane.INFORMATION_MESSAGE);                
        }else{
            valor = false;
             JOptionPane.showMessageDialog(this, "La Fecha de Pago no puede ser anterior a la de Contrato", "Validación", JOptionPane.INFORMATION_MESSAGE);
        }
     return valor;   
    }
